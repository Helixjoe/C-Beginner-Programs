#include <iostream>
using namespace std;
// Check Whether Consonant or Vowel
int main() {
    
    char alph;
    cout<<"Enter Alphabet"<<endl;
    cin>>alph;
    
    switch(alph){
        
        case'a':
        cout<<"Alphabet is a Vowel"<<endl;
        break;
        
        case'e':
        cout<<"Alphabet is a Vowel"<<endl;
        break;
        
        case'i':
        cout<<"Alphabet is a Vowel"<<endl;
        break;
        
        case'o':
        cout<<"Alphabet is a Vowel"<<endl;
        break;
        
        case'u':
        cout<<"Alphabet is a Vowel"<<endl;
        break;
        
        default:
        cout<<"Alphabet is a Consonant"<<endl;
        break;
    }

    
    
    
    
        
	return 0;
}
